
public class pattern2 {
		public static void main(String[] args) {
			int n = 4;
			for (int j = 0; j <= n; j++) {

				for (int i = 1; i <= n; i++) {
					System.out.print(" * ");
				}
				System.out.println(" ");
			}
		}
	}
